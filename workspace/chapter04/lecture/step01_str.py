
# 자료구조
# 1.순서자료구조
# 1-1.str 클래스 형식
str_var=str(object="string")
print(str_var)  #string
print(type(str_var))  #<class 'str'>
print(str_var[0])  #s
print(str_var[-1])  #g

# 1-2.str 클래스 간편 형식
str_var2='string'
print(str_var2)  #string
print(type(str_var2))  #<class 'str'>
print(str_var2[0])  #s
print(str_var2[-1])  #g



